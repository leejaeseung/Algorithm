int mv1[4] = { 0, 1, 0, -1 };
int mv2[4] = { 1, 0, -1, 0 };
int mv_all1[8] = { 0, 1, 0, -1, -1, -1, 1, 1 };
int mv_all2[8] = { 1, 0, -1, 0 , -1, 1, -1, 1 };

int n, k, l;
int maps[101][101];
deque<pii> snake;

int main(void) {
	FIO;

	cin >> n >> k;

	for (int i = 0; i < k; i++)
	{
		int x, y;
		cin >> x >> y;
		maps[x][y] = -1;
	}

	pii head = { 1, 1 };
	snake.push_back(head);
	maps[1][1] = 1;
	int dir = 0;

	cin >> l;

	int ans = 0;

	for (int i = 0; i <= l; i++)
	{
		int x;
		char d;
		if(i < l)
			cin >> x >> d;
		else {
			x = 10001;
			d = 'L';
		}

		while(ans < x)
		{
			ans++;
			int nx = head.first + mv1[dir];
			int ny = head.second + mv2[dir];

			if (nx > n || ny > n || nx <= 0 || ny <= 0 || maps[nx][ny] == 1) {
				cout << ans;
				exit(0);
			}

			head = { nx, ny };
			snake.push_back(head);

			if (maps[nx][ny] == 0) {
				pii tail = snake.front();
				snake.pop_front();

				maps[tail.first][tail.second] = 0;
			}

			maps[nx][ny] = 1;
		}

		if (d == 'D') {
			dir = (dir + 1) % 4;
		}
		else {
			dir = (dir + 3) % 4;
		}
	}

}