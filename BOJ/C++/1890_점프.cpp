int mv1[4] = { 0, 1, 0, -1 };
int mv2[4] = { 1, 0, -1, 0 };
int mv_all1[8] = { 0, 1, 0, -1, -1, -1, 1, 1 };
int mv_all2[8] = { 1, 0, -1, 0 , -1, 1, -1, 1 };

int n;
int maps[101][101];
ll dp[101][101];

ll dfs(int x, int y) {

	ll& ret = dp[x][y];

	if (ret != -1)	return ret;

	ret = 0;
	if (x == n - 1 && y == n - 1)	return ret = 1;

	for (int i = 0; i < 2; i++)
	{
		int nx = x + mv1[i] * maps[x][y];
		int ny = y + mv2[i] * maps[x][y];

		if (nx < 0 || ny < 0 || nx >= n || ny >= n)	continue;

		ret += dfs(nx, ny);
	}

	return ret;
}

